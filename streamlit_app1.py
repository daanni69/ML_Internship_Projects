import streamlit as st
import joblib

# Load model and vectorizer
model = joblib.load("spam_emails_model.pkl")
vectorizer = joblib.load("vectorizer.pkl")

# UI Title
st.title("üìß Email Spam Classifier")

# Input
email = st.text_area("Enter the email content here:")

if st.button("Predict"):
    clean_email = email.lower().strip()
    vectorized = vectorizer.transform([clean_email])
    prediction = model.predict(vectorized)[0]

    if prediction == 1:
        st.error("‚ùå Spam Email")
    else:
        st.success("‚úÖ Not Spam Email")